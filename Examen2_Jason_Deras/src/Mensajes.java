
import java.util.Date;

public class Mensajes {

    private Date hora;
    private String leido;

    public Mensajes() {
    }

    public Date getHora() {
        return hora;
    }

    public void setHora(Date hora) {
        this.hora = hora;
    }

    public String getLeido() {
        return leido;
    }

    public void setLeido(String leido) {
        this.leido = leido;
    }
    
}
